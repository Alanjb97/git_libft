int	ft_strcmp(const char *s1, const char *s2)
{
	int j;

	j = 0;
	while (s1[j] && s2[j] && s1[j] == s2[j])
		++j;
	return ((unsigned char)s1[j] - (unsigned char)s2[j]);
}
